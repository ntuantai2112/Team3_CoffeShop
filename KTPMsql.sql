CREATE TABLE PHONGHOC(
MAPHONGHOC VARCHAR(10),
TENPHONGHOC NVARCHAR(30),
TOANHA NVARCHAR(20),
PRIMARY KEY (MAPHONGHOC)
)

CREATE TABLE HOADON(
NUMORDER INT IDENTITY(1,1),
MAHD AS ('HD'+CONVERT([varchar](10),[NUMORDER])) PERSISTED NOT NULL,
MAVI UNIQUEIDENTIFIER REFERENCES VIHOCPHI (MAVI),
MASV VARCHAR(12) REFERENCES SINHVIEN (MASV),
SOTIEN MONEY,
NGAYGD DATE,
GIOGD TIME,
CHUTHICH NVARCHAR(50),
PRIMARY KEY (MAHD)
);

CREATE TABLE VIHOCPHI(
NUMORDER INT IDENTITY(1,1),
MAVI uniqueidentifier,
MASV VARCHAR(12) REFERENCES SINHVIEN (MASV),
SODUHOCPHI MONEY,
PRIMARY KEY (MAVI)
);

CREATE TABLE DIEMTHANHPHAN(
MASV VARCHAR(12) REFERENCES SINHVIEN (MASV),
MAMONHOC VARCHAR(10) REFERENCES MONHOC (MAMONHOC),
MACHUYENNGANH VARCHAR(10) REFERENCES CHUYENNGANH (MACHUYENNGANH),
MALOPHOC VARCHAR(10) REFERENCES LOPHOC (MALOPHOC),
NAM INT,
MUA NVARCHAR(20),
LAB1 FLOAT,
LAB2 FLOAT,
LAB3 FLOAT,
LAB4 FLOAT,
ASSIGNMENT FLOAT,
DIEMTHI FLOAT,
PRIMARY KEY (MASV, MAMONHOC, MACHUYENNGANH, MALOPHOC, NAM, MUA)
);

