CREATE TABLE MONHOC(
NUMORDER INT IDENTITY(1,1),
MAMONHOC VARCHAR(10),
MACHUYENNGANH VARCHAR(10) REFERENCES CHUYENNGANH (MACHUYENNGANH),
MALOPHOC VARCHAR(10) REFERENCES LOPHOC (MALOPHOC),
TENMONHOC NVARCHAR(30),
SOTINCHI INT,
NAM INT,
MUA VARCHAR(10),
PRIMARY KEY (MAMONHOC)
);

CREATE TABLE LOPHOC(
MALOPHOC VARCHAR(10),
MACHUYENNGANH VARCHAR(10) REFERENCES CHUYENNGANH(MACHUYENNGANH),
TENLOPHOC VARCHAR(20),
NAM int,
MUA varchar(10),
PRIMARY KEY (MALOPHOC)
); 

CREATE TABLE CHUYENNGANH(
MACHUYENNGANH VARCHAR(10),
TENCHUYENNGANH NVARCHAR(30),
COSO NVARCHAR(10),
THOIGIANDAOTAO INT,
PRIMARY KEY (MACHUYENNGANH)
);

CREATE TABLE LICH(
MASV VARCHAR(12) REFERENCES SINHVIEN (MASV),
MAMONHOC VARCHAR(10) REFERENCES MONHOC (MAMONHOC),
MALOPHOC VARCHAR(10) REFERENCES LOPHOC (MALOPHOC),
MAPHONGHOC VARCHAR(10) REFERENCES PHONGHOC (MAPHONGHOC),
MAGIANGVIEN VARCHAR(12) REFERENCES GIANGVIEN (MAGV),
MACHUYENNGANH VARCHAR(10) REFERENCES CHUYENNGANH (MACHUYENNGANH),
NAM INT,
MUA VARCHAR(10),
NGAYHOC DATE,
THOIGIAN VARCHAR(30),
LOAILICH NVARCHAR(30),
PRIMARY KEY (MASV, MAMONHOC, MALOPHOC, MAPHONGHOC, MAGIANGVIEN, MACHUYENNGANH, NAM, MUA)
