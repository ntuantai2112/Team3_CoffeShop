);

CREATE TABLE SINHVIEN(
MATKHAU VARCHAR(30),
VAITRO NVARCHAR(20),
ANH IMAGE,
NUMORDER INT IDENTITY(1,1),
MASV AS ('sv'+CONVERT([varchar](10),[NUMORDER])) PERSISTED NOT NULL,
HOTEN NVARCHAR(30),
GIOITINH INT,
NGAYSINH DATE,
EMAIL VARCHAR(30),
SDT VARCHAR(15),
DIACHI NVARCHAR(50),
TRANGTHAI NVARCHAR(20),
NIENKHOA INT,
MACHUYENNGANH VARCHAR(10) REFERENCES CHUYENNGANH (MACHUYENNGANH),
