CREATE TABLE CANBO(
MATKHAU VARCHAR(30),
VAITRO NVARCHAR(20),
NUMORDER INT IDENTITY(1,1),
MACB AS ('cb'+CONVERT([varchar](10),[NUMORDER])) PERSISTED NOT NULL,
HOTEN NVARCHAR(20),
