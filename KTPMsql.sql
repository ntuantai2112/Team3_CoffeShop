CREATE TABLE GIANGVIEN(
MATKHAU VARCHAR(30),
VAITRO NVARCHAR(20),
ANH IMAGE,
NUMORDER INT IDENTITY(1,1),
MAGV AS ('gv'+CONVERT([varchar](10),[NUMORDER])) PERSISTED NOT NULL,
HOTEN NVARCHAR(20),
GIOITINH INT,
NGAYSINH DATE,
